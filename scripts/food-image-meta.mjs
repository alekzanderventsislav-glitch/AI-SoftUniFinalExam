const CATEGORY_FALLBACK = {
  fruits: 'fruit food',
  vegetables: 'vegetable food',
  meats: 'meat food',
  dairy: 'dairy food',
  grains: 'grain food',
  nuts: 'nuts food',
  legumes: 'legumes food',
  fish: 'fish seafood',
};

export function transliterateBg(text) {
  return text
    .toLowerCase()
    .split('')
    .map((char) => CYRILLIC_MAP[char] ?? char)
    .join('');
}

export function foodSlug(name) {
  return transliterateBg(name)
    .toLowerCase()
    .replace(/%/g, 'procent')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

export function getSearchTerm(name, category) {
  return SEARCH_EN[name] || `${transliterateBg(name)} ${CATEGORY_FALLBACK[category] || 'food'}`;
}

export function getFoodImageFileName(name) {
  return `${foodSlug(name)}.jpg`;
}
